(function () {
  "use strict";
  const storageKey = "openformosa-language";
  const defaultLanguage = "zh-Hant";
  const englishTitle = document.title;
  const descriptionMeta = document.querySelector('meta[name="description"]');
  const englishDescription = descriptionMeta ? descriptionMeta.getAttribute("content") : "";

  function normalizeLanguage(value) {
    if (!value) return defaultLanguage;
    const lower = value.toLowerCase();
    if (lower === "en" || lower.startsWith("en-")) return "en";
    if (lower === "zh" || lower.startsWith("zh-")) return "zh-Hant";
    return defaultLanguage;
  }

  function currentLanguage() {
    return normalizeLanguage(localStorage.getItem(storageKey) || defaultLanguage);
  }

  function translation(key, fallback) {
    const lang = currentLanguage();
    if (lang === "en") return fallback;
    return Object.prototype.hasOwnProperty.call(OpenFormosaZh, key) ? OpenFormosaZh[key] : fallback;
  }

  function remember(el, datasetKey, getter) {
    if (!Object.prototype.hasOwnProperty.call(el.dataset, datasetKey)) {
      el.dataset[datasetKey] = getter();
    }
    return el.dataset[datasetKey];
  }

  function setText(el) {
    const fallback = remember(el, "i18nDefault", () => el.textContent);
    el.textContent = translation(el.dataset.i18n, fallback);
  }

  function setAttribute(el, dataName, attrName, fallbackKey) {
    const key = el.dataset[dataName];
    if (!key) return;
    const fallback = remember(el, fallbackKey, () => el.getAttribute(attrName) || "");
    el.setAttribute(attrName, translation(key, fallback));
  }

  function applyLanguage(language) {
    const lang = normalizeLanguage(language);
    localStorage.setItem(storageKey, lang);
    document.documentElement.lang = lang;
    document.documentElement.dataset.lang = lang;
    document.querySelectorAll("[data-i18n]").forEach(setText);
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => setAttribute(el, "i18nPlaceholder", "placeholder", "i18nPlaceholderDefault"));
    document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => setAttribute(el, "i18nAriaLabel", "aria-label", "i18nAriaLabelDefault"));
    document.querySelectorAll("[data-i18n-alt]").forEach((el) => setAttribute(el, "i18nAlt", "alt", "i18nAltDefault"));

    const pageKey = document.body.dataset.pageKey;
    if (lang === "zh-Hant" && pageKey && OpenFormosaZh[`title_${pageKey}`]) {
      document.title = OpenFormosaZh[`title_${pageKey}`];
    } else {
      document.title = englishTitle;
    }
    if (descriptionMeta) {
      const zhDescription = pageKey ? OpenFormosaZh[`desc_${pageKey}`] : "";
      descriptionMeta.setAttribute("content", lang === "zh-Hant" && zhDescription ? zhDescription : englishDescription);
    }

    document.querySelectorAll("[data-lang-option]").forEach((button) => {
      const active = button.dataset.langOption === lang;
      button.classList.toggle("is-active", active);
      button.setAttribute("aria-pressed", String(active));
    });
    window.dispatchEvent(new CustomEvent("openformosa:language", { detail: { language: lang } }));
  }

  window.OpenFormosaI18n = {
    applyLanguage,
    getLanguage: currentLanguage,
    text(key, fallback) {
      return translation(key, fallback || key);
    }
  };

  document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll("[data-lang-option]").forEach((button) => {
      button.addEventListener("click", () => applyLanguage(button.dataset.langOption));
    });
    applyLanguage(currentLanguage());
  });
})();
